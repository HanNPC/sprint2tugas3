function arrayToObject(fullName, firstName , lastName, gender, age ){
    var abduh = {}
    abduh.fullName = fullName,
    abduh.firstName = firstName,
    abduh.lastName = lastName,
    abduh.gender = gender,
    abduh.age = age
    return abduh
}
console.log(arrayToObject ('Abduh Muhammad' , 'Abduh', 'Muhammad', 'male', 28 ))


